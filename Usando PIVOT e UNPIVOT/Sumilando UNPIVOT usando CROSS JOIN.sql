-- Criando e inserindo dados na tabela
DECLARE @TBL_TOTAL_VENDAS AS TABLE
(
    PRODUTO VARCHAR(100),
    TOTAL_VENDAS_2022 DECIMAL(10,2),
	TOTAL_VENDAS_2023 DECIMAL(10,2),
	TOTAL_VENDAS_2024 DECIMAL(10,2)
);

INSERT INTO
	@TBL_TOTAL_VENDAS (PRODUTO, TOTAL_VENDAS_2022, TOTAL_VENDAS_2023, TOTAL_VENDAS_2024)
VALUES
	('Notebook', 15000.00, 18000.00, 20000.00),
	('Smartphone', 22000.00, 25000.00, 30000.00),
	('Tablet', 7000.00, 9000.00, 9500.00),
	('Headset', 4000.00, 5000.00, 6000.00),
	('Monitor', 8000.00, 10000.00, 12000.00);

-- Exemplo
SELECT
	PRODUTO,
	ANO,
	TOTAL_VENDAS
FROM
	@TBL_TOTAL_VENDAS
	CROSS APPLY(
		VALUES
			('2022', TOTAL_VENDAS_2022),
			('2023', TOTAL_VENDAS_2023),		
			('2024', TOTAL_VENDAS_2024)
	) AS TB(ANO, TOTAL_VENDAS);